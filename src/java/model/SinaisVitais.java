package model;

public class SinaisVitais {
 
	private int id;
	 
	private double frRepouso;
	 
	private double paSistolica;
	 
	private double paDiastolica;
	 
	private double fcRepouso;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getFrRepouso() {
        return frRepouso;
    }

    public void setFrRepouso(double frRepouso) {
        this.frRepouso = frRepouso;
    }

    public double getPaSistolica() {
        return paSistolica;
    }

    public void setPaSistolica(double paSistolica) {
        this.paSistolica = paSistolica;
    }

    public double getPaDiastolica() {
        return paDiastolica;
    }

    public void setPaDiastolica(double paDiastolica) {
        this.paDiastolica = paDiastolica;
    }

    public double getFcRepouso() {
        return fcRepouso;
    }

    public void setFcRepouso(double fcRepouso) {
        this.fcRepouso = fcRepouso;
    }
	 
	 
}
 
