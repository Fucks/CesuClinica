package model;

public class Inspecao {
 
	private int id;
	 
	private boolean deformidades;
	 
	private boolean contratura;
	 
	private boolean edema;
	 
	private boolean cicatriz;
	 
	private boolean encurtamento;
	 
	private boolean rigidez;
	 
	private boolean cianose;
	 
	private boolean espasmo;
	 
	private boolean ortese;
	 
	private String observacoes;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public boolean isDeformidades() {
        return deformidades;
    }

    public void setDeformidades(boolean deformidades) {
        this.deformidades = deformidades;
    }

    public boolean isContratura() {
        return contratura;
    }

    public void setContratura(boolean contratura) {
        this.contratura = contratura;
    }

    public boolean isEdema() {
        return edema;
    }

    public void setEdema(boolean edema) {
        this.edema = edema;
    }

    public boolean isCicatriz() {
        return cicatriz;
    }

    public void setCicatriz(boolean cicatriz) {
        this.cicatriz = cicatriz;
    }

    public boolean isEncurtamento() {
        return encurtamento;
    }

    public void setEncurtamento(boolean encurtamento) {
        this.encurtamento = encurtamento;
    }

    public boolean isRigidez() {
        return rigidez;
    }

    public void setRigidez(boolean rigidez) {
        this.rigidez = rigidez;
    }

    public boolean isCianose() {
        return cianose;
    }

    public void setCianose(boolean cianose) {
        this.cianose = cianose;
    }

    public boolean isEspasmo() {
        return espasmo;
    }

    public void setEspasmo(boolean espasmo) {
        this.espasmo = espasmo;
    }

    public boolean isOrtese() {
        return ortese;
    }

    public void setOrtese(boolean ortese) {
        this.ortese = ortese;
    }

    public String getObservacoes() {
        return observacoes;
    }

    public void setObservacoes(String observacoes) {
        this.observacoes = observacoes;
    }
	 
	
}
 
